#include "libft.h"

int	ft_atoi(const char *str)
{
	int	result;
	int	i;
	int	neg;

	i = 0;
	neg = 1;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
		neg = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = 10 * result + (str[i] - 48);
		i++;
	}
	return (neg * result);
}
